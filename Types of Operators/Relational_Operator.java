class Relational_Operator
{
	public static void main(String args[])
	{
	    int a=10, b=20, c=30, d=40;
	    
	    System.out.println(a>b);
	    System.out.println(b<c);
	    System.out.println(c==a);
	    System.out.println(b!=a);
	    System.out.println(b>=a);
	    System.out.println(c<=d);
	}    
}